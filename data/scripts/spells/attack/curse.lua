local combat = Combat()
combat:setParameter(COMBAT_PARAM_TYPE, COMBAT_DEATHDAMAGE)
combat:setParameter(COMBAT_PARAM_EFFECT, CONST_ME_SMALLCLOUDS)
combat:setParameter(COMBAT_PARAM_DISTANCEEFFECT, CONST_ANI_DEATH)

local condition = Condition(CONDITION_CURSED)
condition:setParameter(CONDITION_PARAM_DELAYED, 1)

condition:addDamage(1, 3000, -90)
condition:addDamage(1, 3000, -80)
condition:addDamage(1, 3000, -70)
condition:addDamage(1, 3000, -68)
condition:addDamage(2, 3000, -66)
condition:addDamage(2, 3000, -64)
condition:addDamage(2, 3000, -62)
condition:addDamage(2, 3000, -60)
condition:addDamage(3, 3000, -58)
condition:addDamage(3, 3000, -50)
condition:addDamage(3, 3000, -48)
condition:addDamage(4, 3000, -46)
condition:addDamage(4, 3000, -40)
condition:addDamage(5, 3000, -38)
condition:addDamage(5, 3000, -30)
condition:addDamage(6, 3000, -20)
condition:addDamage(10, 3000, -10)
combat:addCondition(condition)

local spell = Spell("instant")

function spell.onCastSpell(creature, var)
	return combat:execute(creature, var)
end

spell:group("attack")
spell:id(139)
spell:name("Curse")
spell:words("utori mort")
spell:castSound(SOUND_EFFECT_TYPE_SPELL_OR_RUNE)
spell:impactSound(SOUND_EFFECT_TYPE_SPELL_CURSE)
spell:level(75)
spell:mana(30)
spell:isAggressive(true)
spell:range(3)
spell:needTarget(true)
spell:blockWalls(true)
spell:cooldown(10 * 1000)
spell:groupCooldown(1 * 1000)
spell:needLearn(false)
spell:vocation("sorcerer;true", "master sorcerer;true")
spell:register()
